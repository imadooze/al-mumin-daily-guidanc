// دالة للبحث في الأحاديث
export const searchAhadith = (searchTerm: string, category: string = 'all'): Hadith[] => {
  return ahadithDatabase.filter(hadith => {
    const matchesSearch = !searchTerm || 
      hadith.title.includes(searchTerm) || 
      hadith.arabic.includes(searchTerm) ||
      hadith.narrator.includes(searchTerm);
    
    const matchesCategory = category === 'all' || hadith.category === category;
    
    return matchesSearch && matchesCategory;
  });
};

// دالة للحصول على حديث عشوائي
export const getRandomHadith = (): Hadith => {
  const randomIndex = Math.floor(Math.random() * ahadithDatabase.length);
  return ahadithDatabase[randomIndex];
};

// دالة للحصول على أحاديث حسب الفئة
export const getAhadithByCategory = (category: string): Hadith[] => {
  if (category === 'all') return ahadithDatabase;
  return ahadithDatabase.filter(hadith => hadith.category === category);
};