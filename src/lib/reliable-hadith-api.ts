export const getRandomHadith = (): Hadith => {
  const randomIndex = Math.floor(Math.random() * ahadithDatabase.length);
  return ahadithDatabase[randomIndex];
};

export const searchAhadith = (searchTerm: string): Hadith[] => {
  if (!searchTerm.trim()) return ahadithDatabase;
  
  return ahadithDatabase.filter(hadith =>
    hadith.hadith.includes(searchTerm) ||
    hadith.book.includes(searchTerm) ||
    hadith.narrator?.includes(searchTerm) ||
    hadith.reference?.includes(searchTerm)
  );
};

export const getHadithById = (id: number): Hadith | undefined => {
  return ahadithDatabase.find(hadith => hadith.id === id);
};